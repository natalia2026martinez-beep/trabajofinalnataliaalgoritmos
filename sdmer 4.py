#Dia de la semana 
numero_de_la_semana=int(input("numero de la semana:"))
if numero_de_la_semana==1:
    print("lunes")  
elif numero_de_la_semana==2:
    print("martes")     
elif numero_de_la_semana==3:
    print("miercoles")

elif numero_de_la_semana==4:
    print("jueves")
elif numero_de_la_semana==5:
    print("viernes")
elif numero_de_la_semana==6:
    print("sabado")
elif numero_de_la_semana==7:
    print("domingo")
elif numero_de_la_semana<1 or numero_de_la_semana>7:
    print("numero invalido")
    



